import { Terminal } from "@xterm/xterm";
import { FitAddon } from "@xterm/addon-fit";
import "@xterm/xterm/css/xterm.css";
import "./styles.css";

type MeResponse = { authenticated: boolean; username?: string };

type LayoutSurface = {
  id: string;
  title?: string;
  focused?: boolean;
  snapshot?: string;
  cols?: number;
  rows?: number;
  cursorX?: number;
  cursorY?: number;
  x?: number;
  y?: number;
  w?: number;
  h?: number;
};

type LayoutTab = {
  index: number;
  title?: string;
  focusedSurfaceId?: string;
  surfaces: LayoutSurface[];
};

type RelayMessage = {
  type?: unknown;
  data?: unknown;
  encoding?: unknown;
  message?: unknown;
  surfaceId?: unknown;
  activeTab?: unknown;
  tabs?: unknown;
};

type SurfaceView = {
  panel: HTMLElement;
  title: HTMLSpanElement;
  meta: HTMLElement;
  host: HTMLDivElement;
  term: Terminal;
  fit: FitAddon;
  decoder: TextDecoder;
  disposeInput: { dispose(): void };
  resizeObserver: ResizeObserver | null;
  fitQueued: boolean;
  hasLiveOutput: boolean;
  snapshotApplied: boolean;
  opened: boolean;
  pendingOutput: string;
  remoteCols: number | null;
  remoteRows: number | null;
};

type ControlState = "idle" | "pending" | "granted";
const SESSION_KEY_STORAGE_KEY = "phantty.remote.sessionKey";

const appRoot = document.querySelector<HTMLElement>("#app");

if (!appRoot) {
  throw new Error("Missing app root");
}

const app: HTMLElement = appRoot;
const encoder = new TextEncoder();

let socket: WebSocket | null = null;
let layoutState: { activeTab: number; tabs: LayoutTab[] } | null = null;
let selectedTabIndex = 0;
let selectedSurfaceId: string | null = null;
let surfaceViews = new Map<string, SurfaceView>();
let notices: string[] = [];
let controlState: ControlState = "idle";
let hasSeenLayout = false;

function api(path: string, init?: RequestInit): Promise<Response> {
  return fetch(path, {
    credentials: "same-origin",
    headers: { "content-type": "application/json", ...(init?.headers ?? {}) },
    ...init,
  });
}

async function loadMe(): Promise<MeResponse> {
  const res = await api("/api/me");
  if (!res.ok) return { authenticated: false };
  return res.json() as Promise<MeResponse>;
}

