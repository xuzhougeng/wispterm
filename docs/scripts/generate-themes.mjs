import { readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const here = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(here, "..", "..");
const themesDir = path.join(repoRoot, "src", "themes");
const outFile = path.join(repoRoot, "docs", "themes-data.js");

const colorKeys = new Set([
  "background",
  "foreground",
  "cursor-color",
  "cursor-text",
  "selection-background",
  "selection-foreground",
]);

function normalizeColor(value) {
  const trimmed = value.trim();
  const match = trimmed.match(/^#?([0-9a-fA-F]{6})$/);
  return match ? `#${match[1].toLowerCase()}` : null;
}

function relativeLuminance(hex) {
  const rgb = [1, 3, 5].map((offset) => Number.parseInt(hex.slice(offset, offset + 2), 16) / 255);
  const linear = rgb.map((channel) => {
    if (channel <= 0.03928) return channel / 12.92;
    return ((channel + 0.055) / 1.055) ** 2.4;
  });
  return 0.2126 * linear[0] + 0.7152 * linear[1] + 0.0722 * linear[2];
}

function parseTheme(name, text) {
  const colors = {};
  const palette = Array.from({ length: 16 }, () => "#000000");

  for (const rawLine of text.split(/\r?\n/)) {
    const line = rawLine.trim();
    if (!line || line.startsWith("#")) continue;

    const paletteMatch = line.match(/^palette\s*=\s*(\d+)\s*=\s*(#?[0-9a-fA-F]{6})\s*$/);
    if (paletteMatch) {
      const index = Number.parseInt(paletteMatch[1], 10);
      const color = normalizeColor(paletteMatch[2]);
      if (index >= 0 && index < palette.length && color) palette[index] = color;
      continue;
    }

    const keyValue = line.match(/^([a-zA-Z0-9_-]+)\s*=\s*(.+?)\s*$/);
    if (!keyValue) continue;
    const [, key, rawValue] = keyValue;
    if (!colorKeys.has(key)) continue;

    const color = normalizeColor(rawValue);
    if (color) colors[key] = color;
  }

  const background = colors.background ?? palette[0];
  const foreground = colors.foreground ?? palette[7];
  const selection = colors["selection-background"] ?? palette[8];
  const cursor = colors["cursor-color"] ?? foreground;

  return {
    name,
    background,
    foreground,
    cursor,
    selection,
    kind: relativeLuminance(background) >= 0.5 ? "light" : "dark",
    palette,
  };
}

const files = (await readdir(themesDir, { withFileTypes: true }))
  .filter((entry) => entry.isFile())
  .map((entry) => entry.name)
  .sort((a, b) => a.localeCompare(b, "en", { sensitivity: "base" }));

const themes = [];
for (const file of files) {
  const text = await readFile(path.join(themesDir, file), "utf8");
  themes.push(parseTheme(file, text));
}

const banner = `// Generated by docs/scripts/generate-themes.mjs from src/themes.\n`;
const payload = `window.WISPTERM_THEMES = ${JSON.stringify(themes, null, 2)};\n`;
await writeFile(outFile, banner + payload, "utf8");
console.log(`generated ${path.relative(repoRoot, outFile)} with ${themes.length} themes`);
